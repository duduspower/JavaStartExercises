public class Ex8 {
    public static void main(String[] args) {
        double a = -6;
        double b = 5;

        System.out.println("Wyniki : ");
        System.out.println("A do potęgi B : " + Math.pow(a,b));
        System.out.println("Wartość bezwzglęna z liczby A : " + Math.abs(a));
        System.out.println("Pierwiastek z liczby A do potęgi B" + (double)Math.sqrt(Math.pow(a,b)));
    }
}
